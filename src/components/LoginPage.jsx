import React, { useEffect, useRef } from 'react';
import { auth, googleProvider, signInWithPopup } from '../firebase'; // Import from your firebase config
import lottie from 'lottie-web';
import './LoginPage.css';

export default function LoginPage() {
  const animationContainer = useRef(null);

  useEffect(() => {
    let anim;
    if (animationContainer.current) {
      anim = lottie.loadAnimation({
        container: animationContainer.current,
        renderer: 'svg',
        loop: true,
        autoplay: true,
        path: '/lottie/X11-login.json'
      });
    }

    return () => {
      if (anim) {
        anim.destroy();
      }
    };
  }, []);

  const handleGoogleLogin = async () => {
    try {
      console.log('üîÑ Starting Google login...');
      const result = await signInWithPopup(auth, googleProvider);
      console.log('‚úÖ Login successful:', result.user.email);
      // Auth state change will be handled by onAuthStateChanged in App.jsx
    } catch (error) {
      console.error('‚ùå Google login error:', error);
      alert(`Login failed: ${error.message}`);
    }
  };

  return (
    <div className="login-page">
      {/* Cosmic Sky Background */}
      <div className="cosmic-background">
        {/* Background Stars */}
        <div className="background-stars">
          {Array.from({ length: 80 }).map((_, i) => (
            <div
              key={i}
              className="background-star"
              style={{
                left: `${Math.random() * 100}%`,
                top: `${Math.random() * 100}%`,
                width: `${Math.random() * 0.8 + 0.2}px`,
                height: `${Math.random() * 0.8 + 0.2}px`,
                opacity: Math.random() * 0.3 + 0.1,
                animationDuration: `${Math.random() * 20 + 10}s`
              }}
            />
          ))}
        </div>

        {/* Lottie Animation */}
        <div className="login-animation-container">
          <div ref={animationContainer} className="lottie-animation" />
        </div>

        {/* Login Content */}
        <div className="login-content">
          <h1 className="app-title">X-11</h1>
          <p className="app-tagline">Explore the Digital Cosmos</p>
          
          <div className="login-card">
            <h2 className="login-title">Welcome, Explorer</h2>
            <p className="login-subtitle">
              Sign in to join the cosmic network
            </p>
            
            <button 
              onClick={handleGoogleLogin}
              className="google-login-btn"
            >
              <div className="google-btn-content">
                <svg className="google-icon" viewBox="0 0 24 24">
                  <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                  <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                  <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                  <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                </svg>
                <span>Continue with Google</span>
              </div>
            </button>

            <div className="login-footer">
              <p className="terms-text">
                By continuing, you agree to our <a href="#">Terms</a> and <a href="#">Privacy Policy</a>
              </p>
            </div>
          </div>

          {/* Constellation Effect */}
          <div className="constellation">
            {Array.from({ length: 15 }).map((_, i) => (
              <div key={i} className="constellation-line" />
            ))}
          </div>
        </div>
      </div>
    </div>
  );
}